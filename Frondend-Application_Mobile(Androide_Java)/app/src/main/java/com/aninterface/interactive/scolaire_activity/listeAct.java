package com.aninterface.interactive.scolaire_activity;

public class listeAct {
    public String actcode;
    public String actnom;
    public String actdes;

    public  listeAct() {

        this.actcode=actcode;
        this.actnom=actnom;
        this.actdes=actdes;
    }
    public String getactcode()
    {
        return actcode;
    }
    public void setactcode (String actcode)
    {
        this.actcode=actcode;
    }

    public String getactnom() {
        return actnom;
    }
    public void setactnom(String actnom) {
        this.actnom = actnom;
    }

    public String getactdes() {
        return actdes;
    }
    public void setactdes(String actdes) {
        this.actdes = actdes;
    }
}
